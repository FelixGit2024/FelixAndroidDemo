package com.firstproject.androiddemofx.model;

public class ModelWeek {
    private int img;
    private String week;

    public ModelWeek(int img, String week) {
        this.img = img;
        this.week = week;
    }

    public int getImg() {
        return img;
    }

    public String getWeek() {
        return week;
    }
}